package com.sshlc.org.domain;

public class Empresas {

	private Integer keyEmp;
	private String descripcion;
	private String sector;
        private String descripcionSector;
        private String status;
        private String reservado;

    /**
     * @return the keyEmp
     */
    public Integer getKeyEmp() {
        return keyEmp;
    }

    /**
     * @param keyEmp the keyEmp to set
     */
    public void setKeyEmp(Integer keyEmp) {
        this.keyEmp = keyEmp;
    }

    /**
     * @return the descripcion
     */
    public String getDescripcion() {
        return descripcion;
    }

    /**
     * @param descripcion the descripcion to set
     */
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    /**
     * @return the sector
     */
    public String getSector() {
        return sector;
    }

    /**
     * @param sector the sector to set
     */
    public void setSector(String sector) {
        this.sector = sector;
    }

    /**
     * @return the descripcionSector
     */
    public String getDescripcionSector() {
        return descripcionSector;
    }

    /**
     * @param descripcionSector the descripcionSector to set
     */
    public void setDescripcionSector(String descripcionSector) {
        this.descripcionSector = descripcionSector;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the reservado
     */
    public String getReservado() {
        return reservado;
    }

    /**
     * @param reservado the reservado to set
     */
    public void setReservado(String reservado) {
        this.reservado = reservado;
    }
    
        
        
}
